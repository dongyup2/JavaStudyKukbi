package j11_클래스2;
	
public class CarBrand {
	private String brand;
	private int age;
	
	public CarBrand() {
		
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}	
